public class UltraPagerAdapter extends PagerAdapter {
    private boolean isMultiScr;

    public int countNumber = 1;

    private List<String> imageUrlArray = new ArrayList<>();;

    public UltraPagerAdapter(boolean isMultiScr) {
        this.isMultiScr = isMultiScr;
    }

    @Override
    public int getCount() {
       return imageUrlArray.size();

    }

    public void addItems(List<String> urls) {
        imageUrlArray.addAll(urls);
        notifyDataSetChanged();
    }

    @Override
    public boolean isViewFromObject(View view, Object object) {
        return view == object;
    }

    @Override
    public Object instantiateItem(ViewGroup container, int position) {
        LinearLayout linearLayout = (LinearLayout) LayoutInflater.from(container.getContext()).inflate(R.layout.layout_child, null);

        ImageView adView = (ImageView) linearLayout.findViewById(R.id.imageView);

        Picasso.get().load(imageUrlArray.get(position)).into(adView);

        container.addView(linearLayout);


        return linearLayout;
    }

    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        LinearLayout view = (LinearLayout) object;
        container.removeView(view);
    }


}